CREATE DATABASE batalha_pokemon;
\c batalha_pokemon;

CREATE TABLE pokemon (
    id SERIAL PRIMARY KEY ,
    nome VARCHAR(255) NOT NULL,
    tipo VARCHAR(255) NOT NULL,
    tipo2 VARCHAR(255),
    nivel INT NOT NULL,
    hp INT NOT NULL,
    ataque INT NOT NULL,
    defesa INT NOT NULL,
    speed INT NOT NULL
);

CREATE TABLE batalha (
    id SERIAL PRIMARY KEY,
    id_pokemon_1 INT NOT NULL,
    id_pokemon_2 INT NOT NULL,
    vencedor INT NOT NULL,
    FOREIGN KEY (id_pokemon_1) REFERENCES pokemon(id),
    FOREIGN KEY (id_pokemon_2) REFERENCES pokemon(id),
    FOREIGN KEY (vencedor) REFERENCES pokemon(id)
);

